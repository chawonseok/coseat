package com.web.dto;

public class Example {
	private String pk_num;
	private String name;
	private int age;
	public String getPk_num() {
		return pk_num;
	}
	public void setPk_num(String pk_num) {
		this.pk_num = pk_num;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
}
