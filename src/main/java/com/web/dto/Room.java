package com.web.dto;

public class Room {
	 private String roomId;
	 private String userCode;
	 private String title;
	 private String usableSeat;
	 private String studentNum;
	 private String roomX;
	 private String roomY;
	 private String roomStatus;
	 private String expireDay;
	public String getRoomId() {
		return roomId;
	}
	public void setRoomId(String roomId) {
		this.roomId = roomId;
	}
	public String getUserCode() {
		return userCode;
	}
	public void setUserCode(String userCode) {
		this.userCode = userCode;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getUsableSeat() {
		return usableSeat;
	}
	public void setUsableSeat(String usableSeat) {
		this.usableSeat = usableSeat;
	}
	public String getStudentNum() {
		return studentNum;
	}
	public void setStudentNum(String studentNum) {
		this.studentNum = studentNum;
	}
	public String getRoomX() {
		return roomX;
	}
	public void setRoomX(String roomX) {
		this.roomX = roomX;
	}
	public String getRoomY() {
		return roomY;
	}
	public void setRoomY(String roomY) {
		this.roomY = roomY;
	}
	public String getRoomStatus() {
		return roomStatus;
	}
	public void setRoomStatus(String roomStatus) {
		this.roomStatus = roomStatus;
	}
	public String getExpireDay() {
		return expireDay;
	}
	public void setExpireDay(String expireDay) {
		this.expireDay = expireDay;
	}
	
	
}
